		<h2>"But we don't call it that..."</h2> 
		
		<p>Don't worry if your institution uses completely different names for things. One of the discoveries of the Linking You project was that places tend to use different names for different things, especially institutions that have been around for a few hundred years. The aim of this glossary is to provide an understanding of what the different terms mean and where they're exactly the same.</p>
		
		<p>Understanding these connections can help you decide on if it's worth implementing redirects to get people to the right place even if they enter a wrong URI, such as <span class="uri">/accommodation</span> instead of <span class="uri">/residential</span>.</p>
	

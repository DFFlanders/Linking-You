	<h1>Glossary</h1>
	
	<p>Every institution will have its own unique terminology for things. This is an attempt to collate just a few of them and provide suggested terms as well as a resource for those looking to build more accurate redirection systems.</p>
	
	<p>If you're interested in the source for this glossary, you can see all the raw data in the <a href="http://lncn.eu/toolkit/institution">Institutional URIs page</a>.</p>
	
	<dl>
	
		<dt>Accommodation</dt>
			<dd>The department responsible for providing and managing university-owned accommodation for students.</dd>
			<dd>The department responsible for managing standards of 3rd-party landlords.</dd>
			<dd>AKA: Residential Services</dd>
			
		<dt>Residential Services</dt>
			<dd>See: Accommodation</dd>
	
	</dl>
	
